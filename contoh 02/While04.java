public class While04 {
    public static void main(String[] args) {
        String kata = "Hello World!";
        int i = 1;
        int bilangan = 1; 

        while (i <= 5) {
            System.out.println(bilangan + " " + kata);
            bilangan++;
            i++;
        } 
    }
}